router.get('/:id', simpleAuth, async (req, res) => {
  try {
    const { id } = req.params;
    
    if (!mongoose.Types.ObjectId.isValid(id)) {
      return res.status(400).json({
        success: false,
        message: 'ID de reserva inv√°lido'
      });
    }

    const reservation = await Reservation.findById(id).lean();

    if (!reservation) {
      return res.status(404).json({
        success: false,
        message: 'Reserva n√£o encontrada'
      });
    }

    res.json({
      success: true,
      data: { reservation }
    });
  } catch (error) {
    console.error('‚ùå Erro ao buscar reserva:', error);
    res.status(500).json({
      success: false,
      message: 'Erro interno do servidor'
    });
  }
});

// ‚úÖ ROTA 4: ATUALIZAR STATUS (ROBUSTA)
router.patch('/:id/status', simpleAuth, async (req, res) => {
  try {
    const { id } = req.params;
    const { status } = req.body;
    
    if (!mongoose.Types.ObjectId.isValid(id)) {
      return res.status(400).json({
        success: false,
        message: 'ID de reserva inv√°lido'
      });
    }

    const allowedStatuses = ['pending', 'confirmed', 'checked-in', 'checked-out', 'cancelled'];
    if (!allowedStatuses.includes(status)) {
      return res.status(400).json({
        success: false,
        message: 'Status inv√°lido',
        allowedStatuses
      });
    }
    
    const reservation = await Reservation.findByIdAndUpdate(
      id,
      { 
        status,
        updatedAt: new Date()
      },
      { new: true }
    );

    if (!reservation) {
      return res.status(404).json({
        success: false,
        message: 'Reserva n√£o encontrada'
      });
    }

    // ‚úÖ ATUALIZAR QUARTO COM BASE NO STATUS
    let roomStatus = 'available';
    
    if (status === 'checked-in') roomStatus = 'occupied';
    else if (status === 'checked-out') roomStatus = 'cleaning';
    
    await atualizarStatusQuarto(reservation.roomId, roomStatus);

    res.json({
      success: true,
      message: `Status atualizado para ${status}`,
      data: { reservation }
    });
  } catch (error) {
    console.error('‚ùå Erro ao atualizar status:', error);
    res.status(500).json({
      success: false,
      message: 'Erro interno do servidor'
    });
  }
});

// ‚úÖ ROTA 5: DELETAR (ROBUSTA)
router.delete('/:id', simpleAuth, async (req, res) => {
  try {
    const { id } = req.params;
    
    if (!mongoose.Types.ObjectId.isValid(id)) {
      return res.status(400).json({
        success: false,
        message: 'ID de reserva inv√°lido'
      });
    }

    const reservation = await Reservation.findByIdAndDelete(id);

    if (!reservation) {
      return res.status(404).json({
        success: false,
        message: 'Reserva n√£o encontrada'
      });
    }

    // ‚úÖ LIBERAR QUARTO
    await atualizarStatusQuarto(reservation.roomId, 'available');

    res.json({
      success: true,
      message: 'Reserva deletada com sucesso'
    });
  } catch (error) {
    console.error('‚ùå Erro ao deletar reserva:', error);
    res.status(500).json({
      success: false,
      message: 'Erro interno do servidor'
    });
  }
});

